function verificarAcento(letra, pos){

    if(pos < 0 && letra == 'A'){
        pos = palavraSecretaSorteada.indexOf("Á");   
    }
    if(pos < 0 && letra == 'A'){
        pos = palavraSecretaSorteada.indexOf("À");   
    }
    if(pos < 0 && letra == 'A'){
        pos = palavraSecretaSorteada.indexOf("Ã");  
    }
    if(pos < 0 && letra == 'A'){
        pos = palavraSecretaSorteada.indexOf("Â");   
    }

    if(pos < 0 && letra == 'E'){
        pos = palavraSecretaSorteada.indexOf("É");   
    }
    if(pos < 0 && letra == 'E'){
        pos = palavraSecretaSorteada.indexOf("Ê");    
    }

    if(pos < 0 && letra == 'I'){
        pos = palavraSecretaSorteada.indexOf("Í");  
    }
 
    if(pos < 0 && letra == 'O'){
        pos = palavraSecretaSorteada.indexOf("Ó");   
    }
    if(pos < 0 && letra == 'O'){
        pos = palavraSecretaSorteada.indexOf("Õ");   
    }
    if(pos < 0 && letra == 'O'){
        pos = palavraSecretaSorteada.indexOf("Ô");    
    }

    if(pos < 0 && letra == 'U'){
        pos = palavraSecretaSorteada.indexOf("Ú");    
    }
    
    return pos;
}

